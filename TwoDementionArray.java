package arrays;

public class TwoDementionArray {

	public static void main(String[] args) {
		//syntax:
		int [][] a = new int[2][3];//new int [ rows] [columns]
		//initalisation:
		int[] [] b = { 
				{1,2,3,4}, 
				{5,6,7},
				{8,9,5,32}};
		System.out.println(b[0][3]);
		System.out.println(b[2][3]);
		
	}

}
